// JavaScript has Primitive types (value types) and Reference types

// Primitive types include: strings, number, boolean, undefined, null

let name = "Adam"; // String literal
let age = 23; // Number literal
let isApproved = true; // Boolean literal (can be true/false)
let firstName = undefined;
let lastName = null;

console.log(name);
console.log(age);
console.log(isApproved);
console.log(firstName);
console.log(lastName);

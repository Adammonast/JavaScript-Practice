// comparion operators are used to compare the value of a variable with something else
// result of an expression that includes a comparison operator is a boolean (true/false)
let x = 1;
// relational operators include:
// greater than operator
console.log("Greater: ", x > 0);
// greater than or equal
console.log("Greater or Equal: ", x >= 0);
// less than
console.log("Less: ", x <= 0);
// less than or equal
console.log("Greater:", x <= 0);

// equality operators ---> return boolean values
// equal to operator
console.log("Equal: ", x === 1);
// not equal to
console.log("Equal: ", x != 1);

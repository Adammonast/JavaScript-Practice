// assignment operators assign values
// variable types and names go to the right of assignment operator, values to the left
let x = 10;
// arithmetic operators can used in combinations assignment operators
// performs operator and assigns values
console.log("Addition: ", (x += 5));
console.log("Subtraction: ", (x -= 5));
console.log("Multiplication: ", (x *= 5));
console.log("Division: ", (x /= 5));
console.log("Remainder: ", (x %= 5));
